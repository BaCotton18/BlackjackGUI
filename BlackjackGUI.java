  public void PlayerWin(){
    PlayerMoney += Bet;
    DealerMoney -= Bet;
  }
  
  public void DealerWin(){
    PlayerMoney -= Bet;
    DealerMoney += Bet;
  }
  
  //Calculates the results of this round.
  public void Result(){
    buttonsVisible();
    lblDealerTotal.setText(Integer.toString(DealerScore));
    lblDHand.setText(DealerCards);
    //Dealer Busts
    if(PlayerScore <= 21 && DealerScore > 21){
      lblStatus.setText("Dealer busted. You win!");
      PlayerWin();
    }
    //Player busts
    else if(PlayerScore > 21 && DealerScore <= 21){
      lblStatus.setText("You busted. Dealer wins...");
      DealerWin();
    }
    //Every either busted or tied
    else if((PlayerScore > 21 && DealerScore > 21) || (PlayerScore == DealerScore) ){
      lblStatus.setText("Nobody wins.");
    }
    //PLayer wins
    else if(PlayerScore > DealerScore){
      lblStatus.setText("You win!");
      PlayerWin();
    }
    //Dealer wins
    else{
      lblStatus.setText("Dealer wins...");
      DealerWin();
    }
    
    lblPlayerMoney.setText(Integer.toString(PlayerMoney));
    lblDealerMoney.setText(Integer.toString(DealerMoney));
    
    if(PlayerMoney == 0){
      lblStatus.setText("You have no more money. Game over.");
      gameEnd();
    }
    else if(DealerMoney == 0){
      lblStatus.setText("The Dealer's out of money. You win!");
      gameEnd();
    }
    
    if(Bet > PlayerMoney){
      Bet = PlayerMoney;
    }
    else if(Bet > DealerMoney){
      Bet = DealerMoney;
    }
    
    lblBet.setText(Integer.toString(Bet));
  }
  
