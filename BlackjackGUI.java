  private void dealerAction(){
    boolean stand = false;
    while(!stand){
      Card card = cardCreate();
      DealerHand.add(card);
      DealerCards += card.getNumDisplay() + " ";
      DealerScore = scoreCount(DealerHand);
      if(DealerScore >= 21){
        stand = true;
      }
      else if(DealerScore >=16 && rand.nextInt(10) > 6){
        stand = true;
      }
      else if(DealerScore >= 12 && rand.nextInt(10) < 3){
        stand = true;
      }
    }
    lblDHand.setText("? ? ? ? ?");
  }
  
