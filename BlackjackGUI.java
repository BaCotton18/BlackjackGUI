  //creates a card. If someone already has this card, then it creates a different card.
  public Card cardCreate(){
    String[] suits = {"Hearts","Clubs","Diamonds","Spades"};
    Card card = new Card(suits[rand.nextInt(4)],rand.nextInt(13)+1);
    if(PlayerHand.contains(card) || DealerHand.contains(card)){
      card = cardCreate();
    }
    return card;
  }
  
  //returns the score for the hand it is given
  public int scoreCount(ArrayList<Card> hand){
    int total = 0;
    int i;
    for(i = 0; i < hand.size(); i++){
      total += hand.get(i).getValue();
    }
    //if the score is over 21 and we have an ace, then we turn the ace's value to 1 instead of 11.
    if(total > 21){
      for(i = 0;i<hand.size();i++){
        if(hand.get(i).getNumDisplay()=="Ace" && hand.get(i).getValue() == 11){
          hand.get(i).setValue(1);
          total -= 10;
        }
      }
    }
    return total;
  }
    
